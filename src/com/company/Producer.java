package com.company;

public class Producer {
    //instance
    private String name;
    private String brand;

    //constructor
    public Producer() {
        this.name = name;
        this.brand = brand;
    }

    //getter setter
    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }
}
